""" Horner's Rule 을 이용하여 다항식 a0 + (a1)x + ... + (an)x^n 을 일차항들의 곱으로만 계산한다. """

################################################################################
# horner:
# input: float x, 다항식들의 계수가 들어있는 tuple coeff
# return: 다항식 coeff 와 (1, x, ..., x^n)의 dot product
################################################################################
def horner(coeff, x):
    
    y = coeff[-2] + x * coeff[-1]
    for i in range(-2, -len(coeff), -1):
        y = coeff[i - 1] + x * y
    
    return y
################################################################################


